// Question 65: Determine the Remainder: Make a function that gets two numbers
//and shows the leftover from dividing them using the % sign. For example,
//remainder(5, 2) should give 1.
// Explain & TIP: The % (modulus) operator finds what's left after one number is
//divided by another. It's handy for figuring out if numbers are even, odd, and more.
//1-  This function Take a two paramaters of numbers -Done
//2-  The leftover from dividing them using the % sign. -Done
function number(num1, num2) {
    return num1 % num2;
}
console.log(number(5, 2));
