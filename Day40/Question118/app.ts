// Question 118: Write a loop that logs numbers from 1 to 10 to the console.

// Explain & TIP: A for loop is a concise way to run a block of code a specific
//number of times. It's perfect for when you know exactly how many iterations
// you need.

// this loop logs numbers from 1 to 10
for (let number: number = 1; number < 11; number++) {
  console.log(number); // out put 1 to 10 numbers
}
